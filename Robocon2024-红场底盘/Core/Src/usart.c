void Send_SerialPlot_Data_Speed(int16_t CurSpeed, int16_t ExpSpeed){
  serialplot_data[0] = 0xFF;
  serialplot_data[1] = *((char*)(&CurSpeed) + 0);
  serialplot_data[2] = *((char*)(&CurSpeed) + 1);
  serialplot_data[3] = *((char*)(&ExpSpeed) + 0);
  serialplot_data[4] = *((char*)(&ExpSpeed) + 1);
  HAL_UART_Transmit(&huart1, serialplot_data, 5, 1000);
}

